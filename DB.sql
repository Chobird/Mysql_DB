--테이블 생성
CREATE TABLE DEPARTMENT (
	DNO NUMBER(4) CONSTRAINT DEPARTMENT_DNO_PK  PRIMARY KEY,
	DNAME VARCHAR2(50) CONSTRAINT DEPARTMENT_DNAME_NN NOT NULL,
	DROOM VARCHAR2(6)
);
CREATE TABLE STUDENT (
	SNO NUMBER(9) CONSTRAINT STUDENT_SNO_PK PRIMARY KEY,
	SNAME VARCHAR2(14) CONSTRAINT STUDENT_SNAME_NN NOT NULL,
	ADDRESS VARCHAR2(100),
	DNO NUMBER(4) CONSTRAINT STUDENT_DNO_FK REFERENCES DEPARTMENT
);

CREATE TABLE PROFESSOR (
	PNO NUMBER(5) CONSTRAINT PROFESSOR_PNO_PK PRIMARY KEY,
	PNAME VARCHAR2(50) CONSTRAINT PROFESSOR_PNAME_NN NOT NULL,
	TEL VARCHAR2(15),
	DNO NUMBER(4) CONSTRAINT PROFESSOR_DNO_FK REFERENCES DEPARTMENT
);

CREATE TABLE SUBJECTS (
	SUBNO NUMBER(10) CONSTRAINT SUBJECTS_SUBNO_PK PRIMARY KEY,
	SUBNAME VARCHAR2(50) CONSTRAINT SUBJECTS_SUBNAME_NN NOT NULL,
	SUBTIME VARCHAR(20) CONSTRAINT SUBJECTS_SUBTIME_NN NOT NULL,
	SUBOPEN VARCHAR2(10) CONSTRAINT SUBJECTS_SUBOPEN_NN NOT NULL,
	SUBROOM VARCHAR2(6),
	STATUS VARCHAR2(20) CONSTRAINT SUBJECTS_STATUS_NN NOT NULL,
	PNO NUMBER(5) CONSTRAINT SUBJECTS_PNO_FK REFERENCES PROFESSOR,
	DNO NUMBER(4) CONSTRAINT SUBJECTS_DNO_FK REFERENCES DEPARTMENT
);

CREATE TABLE COURSE (
	CNO NUMBER(5) CONSTRAINT COURSE_CNO_PK PRIMARY KEY,
	CREDIT NUMBER(3) CONSTRAINT COURSE_CREDIT_NN NOT NULL,
	GRADE VARCHAR2(3) CONSTRAINT COURSE_GRADE_NN NOT NULL,
	SNO NUMBER(9) CONSTRAINT COURSE_SNO_FK REFERENCES STUDENT,
	SUBNO NUMBER(10) CONSTRAINT COURSE_SUBNO_FK REFERENCES SUBJECTS,
	DNO NUMBER(4) CONSTRAINT COURSE_DNO_FK REFERENCES DEPARTMENT

);

